
def plus(a,b):
    try:
        print(a+b)
    except:
        print("error")

def subtract(a,b):
    try:
        print(a-b)
    except:
        print("error")

def div(a,b):
    try:
        print(a%b)
    except:
        print("error")

def multi(a,b):
    try:
        print(a*b)
    except:
        print("error")

